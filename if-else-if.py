


age = int(input("Enter an age between 0-100:"))

if(age>0 and age<=3):
    print("Infant")
elif(age>3 and age<=15):
    print("Child")
elif(age>15 and age<=21):
    print("Teenage")
elif(age>21 and age<=60):
    print("Working Adult")
elif(age>60 and age<=100):
    print("Old age")

